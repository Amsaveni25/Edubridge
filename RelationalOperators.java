package Assignment5;

public class RelationalOperators {

	public static void main(String[] args) {
		int a=12 ,b=5;
		System.out.println("a is "+ a + "b is "+b);
		System.out.println(a==b);//return false
		System.out.println(a!=b);//return true
		System.out.println(a>b);//return true
		System.out.println(a<b);//return false
		System.out.println(a<=b);//return false
		System.out.println(a>=b);//return true
		
	
		

	}

}
